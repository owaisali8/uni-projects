    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Guest g = new Guest(GuestLogin.userInt);
        int Asum = 0;
        int Rsum = 0;
        for(int i= 0; i<g.aList.size(); i++)
            Asum += g.aList.get(i).getPrice();
        for(int i = 0; i<g.roomList.size();i++)
            Rsum += g.roomList.get(i).getPrice();
        
        String s = String.format("Rooms: %s\nAmenities: %s\nTotal: %s Rupees",Rsum,Asum,Rsum+Asum);
        JFrame f=new JFrame();  
        JOptionPane.showMessageDialog(f,s); 
    }//GEN-LAST:event_jButton4ActionPerformed

    private void refreshuserTable(){
        
     DefaultTableModel model = (DefaultTableModel) userTable1.getModel();
    model.setRowCount(0);
    Guest r = new Guest(GuestLogin.userInt); 
    for(int i=0; i<r.roomList.size(); i++){
        Object [] obj = new Object[6];
        obj[0] =r.roomList.get(i).getRoomNumber();
        obj[1] =r.roomList.get(i).getRoomType();
        obj[2] =r.roomList.get(i).getRoomSize();
        obj[3] =r.roomList.get(i).IsRoomAvailable();
        obj[4] =r.roomList.get(i).getHotelName();
        obj[5] =r.roomList.get(i).getPrice();
        model.addRow(obj);
        }
    DefaultTableModel model2 = (DefaultTableModel) userTable2.getModel();
    model2.setRowCount(0);
    
    for(int i=0; i<r.aList.size(); i++){
        Object [] obj = new Object[3];
        obj[0] =r.aList.get(i).getType();
        obj[1] =r.aList.get(i).getPrice();
        obj[2] =r.aList.get(i).getHotel();
        model2.addRow(obj);
    }
    }
    
