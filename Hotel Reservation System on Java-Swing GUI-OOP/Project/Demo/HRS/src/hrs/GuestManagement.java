    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Guest g = new Guest(GuestLogin.userInt);
        Rooms r = g.roomList.remove(userTable1.getSelectedRow());
        g.writeRoomFile();
        Rooms r2 = new Rooms();
        
        for(int i = 0; i<r2.list.size();i++){
            if(r2.list.get(i).getRoomNumber() == r.getRoomNumber()
                    && r2.list.get(i).getPrice() == r.getPrice()){
                r2.list.get(i).changeVacancy();
                r2.list.get(i).setOccupiedName(null);
            }
                
        }
        
        refreshuserTable();
        refresh();
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

