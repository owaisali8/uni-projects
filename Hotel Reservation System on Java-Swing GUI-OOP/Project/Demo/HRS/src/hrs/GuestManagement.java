    private void bookRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookRoomActionPerformed
        // TODO add your handling code here:
        Rooms r = new Rooms();
        if(r.list.get(roomTable.getSelectedRow()).IsRoomAvailable().contains("NO")){
        r.list.get(roomTable.getSelectedRow()).changeVacancy();
        Guest g = new Guest(GuestLogin.userInt);
        g.addRoom(r.list.get(roomTable.getSelectedRow()));
        r.list.get(roomTable.getSelectedRow()).setOccupiedName(g.getName());
        refreshuserTable();
        refresh();
        }
        else{
             JFrame f =new JFrame();   
            JOptionPane.showMessageDialog(f,"Room not Available","Alert",JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_bookRoomActionPerformed

