    protected void refresh(){
    
    DefaultTableModel model = (DefaultTableModel) roomTable.getModel();
    model.setRowCount(0);
    Rooms r = new Rooms(); 
    for(int i=0; i<r.list.size(); i++){
        Object [] obj = new Object[7];
        obj[0] =r.list.get(i).getRoomNumber();
        obj[1] =r.list.get(i).getRoomType();
        obj[2] =r.list.get(i).getRoomSize();
        obj[3] =r.list.get(i).IsRoomAvailable();
        obj[4] =r.list.get(i).getHotelName();
        obj[5] =r.list.get(i).getPrice();
        obj[6] = r.list.get(i).getOccupiedName();
        model.addRow(obj);
        }
    }

