    private void refreshTable2(){
        
    
    DefaultTableModel model = (DefaultTableModel) aTable.getModel();
    model.setRowCount(0);
    Amenities r = new Amenities(); 
    for(int i=0; i<r.list.size(); i++){
        Object [] obj = new Object[3];
        obj[0] =r.list.get(i).getType();
        obj[1] =r.list.get(i).getPrice();
        obj[2] =r.list.get(i).getHotel();
        model.addRow(obj);
    }
    }
