    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = new Rooms().list.get(roomTable.getSelectedRow()).getHotelName();
        HotelInfo h1 = new HotelInfo();
        for (int i=0; i<h1.list.size();i++){
            if(name.equals(h1.list.get(i).getName())){
                JFrame f=new JFrame();
                JOptionPane.showMessageDialog(f,h1.list.get(i).getHotelInfo());
            }
            else{
                JFrame f=new JFrame();
                JOptionPane.showMessageDialog(f,"No Info");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

